def addition(num1, num2)
  sum = num1 + num2
  puts sum
end

def subtraction(num1, num2)
  diff = num1-num2
  puts diff
end

def division(num1, num2)
  div = num1/num2
  puts div
end

def multiplication(num1, num2)
  product = num1 * num2
  puts product
end

def modulo(num1, num2)
  remainder = num1 % num2
  puts remainder
end

def square_root(num)
  root = Math.sqrt(num)
  puts root
end